// Define the city_country function
function city_country(city: string, country: string): string {
    return `${city}, ${country}`;
}

// Call the function with example city-country pairs
let result1 = city_country('Lahore', 'Pakistan');
console.log(result1);  // Output: Lahore, Pakistan

let result2 = city_country('Tokyo', 'Japan');
console.log(result2);  // Output: Tokyo, Japan

let result3 = city_country('Paris', 'France');
console.log(result3);  // Output: Paris, France
